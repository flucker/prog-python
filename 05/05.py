# *
# *   *
# *   *
# *
width = int(input('width = '))
height = int(input('height = '))
row = 1
while row <= height:
    if row == 1 or row == height:
        print('*' * width)
    else:
        print(f'*{' ' * (width - 2)}*')
    row += 1

