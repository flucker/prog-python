# Task 13
# Виведіть на екран «пісочний годинник», максимальна ширина якого зчитується з клавіатури (число непарне). У прикладі ширина дорівнює 5.
# *****
#  ***
#   *
#  ***
# *****
width = int(input('width = '))
if width % 2:
    row = 0
    while row < width:
        if row <= width // 2:
            print(f'{' ' * row}{'*' * (width - row * 2)}{' ' * row}')
        else:
            tmp = width - row - 1
            print(f'{' ' * tmp}{'*' * (width - tmp * 2)}{' ' * tmp}')
        row += 1
else:
    print('Invalid width')